public class Schedule {

    String room;
    String applianceKey;
    String applianceName;
    String applianeId;
    String status;
    String time;
    String repeatMode;

    public String getRoom() {
        return room;
    }

    public void setRoom(String room) {
        this.room = room;
    }

    public String getApplianceKey() {
        return applianceKey;
    }

    public void setApplianceKey(String applianceKey) {
        this.applianceKey = applianceKey;
    }

    public String getApplianceName() {
        return applianceName;
    }

    public void setApplianceName(String applianceName) {
        this.applianceName = applianceName;
    }

    public String getApplianeId() {
        return applianeId;
    }

    public void setApplianeId(String applianeId) {
        this.applianeId = applianeId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getRepeatMode() {
        return repeatMode;
    }

    public void setRepeatMode(String repeatMode) {
        this.repeatMode = repeatMode;
    }
}
